package Model;

/**
 *
 * @author Lappro
 */
public class ThueGay {
    int ID;
    String TenGay;
    String GiaThue;
    String AnhGay;
    
    public ThueGay() {
    }

    public ThueGay(int ID, String TenGay, String GiaThue, String AnhGay) {
        this.ID = ID;
        this.TenGay = TenGay;
        this.GiaThue = GiaThue;
        this.AnhGay = AnhGay;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getTenGay() {
        return TenGay;
    }

    public void setTenGay(String TenGay) {
        this.TenGay = TenGay;
    }

    public String getGiaThue() {
        return GiaThue;
    }

    public void setGiaThue(String GiaThue) {
        this.GiaThue = GiaThue;
    }

    public String getAnhGay() {
        return AnhGay;
    }

    public void setAnhGay(String AnhGay) {
        this.AnhGay = AnhGay;
    }

    

    
    
    
}
